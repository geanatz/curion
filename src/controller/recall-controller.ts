/** Public outcome of the recall controller. */
export type RecallOutcome =
  | {
      status: "answered";
      answer: string;
      /** Memory ids the answer was synthesized from (in rank order). */
      sourceIds: number[];
      /**
       * Internal-only conservative ambiguity signal from the
       * recall-side detector (Phase C).
       *
       * This field is observable to tests and any future
       * structured-content transport, but it is NEVER
       * serialized into the public MCP `text` content block.
       * The `src/tools/recall.ts` projection explicitly drops
       * it; the public message / answer is unchanged.
       *
       * Public-message behaviour on the `ambiguous` verdict
       * is described in spec §5.4 and is implemented in
       * Phase D. The note is generated by
       * `formatAmbiguityNote` in `src/retrieval/ambiguity.ts`.
       */
      internalAmbiguity?: AmbiguitySignal;
      /**
       * Internal-only conservative resolved-history signal
       * from the recall-side detector (Phase H + Phase J).
       *
       * The Architect recommended a **parallel** internal
       * field rather than merging the new variant into the
       * existing `AmbiguitySignal` union: the two detectors
       * have different rule sets (marker-based vs
       * pointer-based), different confidence floors, and
       * different public notes. A merged union would force
       * every consumer to discriminate on `kind` (and
       * treat `resolved-history` as a third variant),
       * with no readability win. A parallel field keeps
       * the Phase D and Phase J code paths structurally
       * independent: the tool layer can compose the two
       * notes via two short, independent helper calls,
       * and the public projection remains a simple
       * `if (note.length > 0) message = note + answer`.
       *
       * This field is observable to tests and any future
       * structured-content transport, but it is NEVER
       * serialized into the public MCP `text` content
       * block. The `src/tools/recall.ts` projection
       * explicitly drops it; the public message / answer
       * is unchanged unless the note (added in Phase J)
       * is prefixed. The note is generated by
       * `formatResolvedHistoryNote` in
       * `src/retrieval/resolved-history.ts`.
       */
      internalResolvedHistory?: ResolvedHistorySignal;
    }
  | { status: "no_memory" }
  | {
      /**
       * Refusal-with-thin-match reroute. The synthesis LLM
       * declined to answer (the `isProviderRefusal` detector
       * fired) but the lexical ranker did surface at least
       * one stored summary above the relevance threshold.
       * The controller surfaces the top-3 curator-voice
       * summaries and a coverage block so the caller has
       * something to inspect instead of a misleading
       * `no_memory` (which says "nothing relevant was
       * found") or a fabricated `answered` (which would
       * leak the refusal prose on the wire).
       *
       * The `summaries` are plain strings (no memory-id
       * reference), capped at 3 by the controller. The
       * `coverage.topScore` is the raw lexical score of
       * the top-ranked candidate (a confidence proxy for
       * the ranker); `coverage.supportingCount` is the
       * count of summaries that passed the ranker
       * threshold, NOT capped by the top-K=3 slice. A
       * future curator summary that contains an id
       * reference would have the same write-path source
       * as the existing `answered.answer` field, so the
       * no-IDs rule is enforced at the schema level (the
       * `summaries` field is structurally a list of
       * strings with no id-bearing child objects), not at
       * the content level.
       */
      status: "weak_match";
      summaries: string[];
      coverage: { topScore: number; supportingCount: number };
    }
  | { status: "rejected"; reason: string; safetyClass: string }
  | { status: "provider_error"; reason: string };

/** Controller options. Most fields have safe defaults. */
export interface RecallControllerOptions {
  /** Minimum lexical score to keep a candidate. Default 0.2. */
  relevanceThreshold?: number;
  /** Max number of top summaries sent to the provider. Default 5. */
  topK?: number;
  /** Optional fetch override for tests. */
  providerFetchImpl?: typeof fetch;
  /** Optional API key override for the primary provider. */
  providerPrimaryApiKey?: string;
  /** Optional API key override for the fallback provider. */
  providerFallbackApiKey?: string;
  /** Optional base URL override for the primary provider. */
  providerPrimaryBaseUrl?: string;
  /** Optional base URL override for the fallback provider. */
  providerFallbackBaseUrl?: string;
  /** Optional model id override for the primary provider. */
  providerPrimaryModel?: string;
  /** Optional model id override for the fallback provider. */
  providerFallbackModel?: string;
  /** Max number of stored summaries scanned. Default 200. */
  storageLimit?: number;
  /**
   * Optional Phase 3A trace plumbing. The tool-boundary handler
   * (and any direct caller that has its own trace run) passes a
   * small context object so the controller can emit
   * `recall.active-memory-read`, `recall.lexical-ranking`, and
   * `recall.selected-candidates` stage events on the SAME trace
   * run that the public tool boundary opens.
   *
   * The context is optional. When omitted (the default), the
   * controller emits no stage events; existing callers are
   * unaffected. The `recordStage` callback is expected to be
   * non-throwing — the controller does not wrap it in a
   * try/catch because the canonical implementation is the
   * existing `ToolBoundaryTracer.recordStage`, which already
   * short-circuits on disabled / closed writers. The controller
   * also short-circuits when `runId` is null, so an already-
   * disabled tracer never sees a call.
   *
   * NOT in this phase:
   *   - Provider I/O capture (request / response bodies).
   *   - Remember-side stage events.
   *   - A CLI / export / purge surface.
   */
  trace?: RecallTraceContext;
}

/**
 * Trace plumbing passed in by the caller. The controller uses
 * this to emit per-stage events on the same run as the public
 * tool-boundary events.
 *
 * `runId` is `null` when the writer is closed or tracing is
 * disabled; the controller checks this before every emission so
 * the disabled path is a true no-op (no allocation, no string
 * formatting, no callback invocation).
 *
 * `recordStage` is the only emission method the controller
 * uses. The kind tag is owned by the controller (the
 * `RECALL_*_KIND` constants in the trace module); the payload
 * is the controller's, the redaction is the writer's.
 */
export interface RecallTraceContext {
  /**
   * Same run id as the public tool-boundary tracer, or `null`
   * when tracing is disabled / writer is closed. The controller
   * only emits when this is non-null.
   */
  runId: number | null;
  /**
   * Non-throwing stage-event recorder. Routes through the
   * existing trace writer; payloads are redacted by the
   * writer's `redactPayload` pass before being persisted.
   */
  recordStage: (kind: string, payload: unknown) => void;
}

