/**
 * Run the narrow MVP `recall` pipeline.
 *
 * Public MCP API is unchanged. This function is consumed by
 * `src/tools/recall.ts`. The controller never throws on expected
 * failure paths; it returns a discriminated union.
 *
 * Important behavior:
 *   - If no stored memory passes the relevance threshold, the
 *     provider is NOT called. We return `no_memory`.
 *   - If the query contains a secret-shaped fragment, the provider
 *     is NOT called and the query is NOT used for retrieval. We
 *     return `rejected` (the public message does not echo the
 *     query). The query text is not logged.
 *   - If the provider fails, we return `provider_error` with a
 *     short, redacted reason. We never fabricate an answer.
 *   - If the provider's answer is empty or contains a
 *     secret-shaped fragment or a raw-dump shape, we return
 *     `provider_error`. We never expose an unsafe public answer.
 */
export async function runRecallController(
  storage: StorageHandle,
  query: string,
  options: RecallControllerOptions = {},
): Promise<RecallOutcome> {
  const threshold = options.relevanceThreshold ?? DEFAULT_RELEVANCE_THRESHOLD;
  const topK = options.topK ?? DEFAULT_TOP_K;
  const storageLimit = options.storageLimit ?? 200;

  // -- 1. Safety pre-check on the query -------------------------------
  //
  // Decision (safer temporary default): reject queries that contain
  // obvious secrets. This matches the remember controller's policy
  // on inputs: secret-shaped fragments must not be forwarded to a
  // provider or used to search storage. The MCP schema already
  // rejects empty queries at the tool boundary; we add this layer
  // as defense in depth. The public message does not echo the
  // query.
  const safety = classifyInput(query);
  logger.debug(
    `recall: pre-check class=${safety.class} reason=${safety.reason}`,
  );
  if (safety.class === "secret") {
    return {
      status: "rejected",
      reason:
        "query contains a secret-shaped fragment; refusing to search or forward it",
      safetyClass: "secret",
    };
  }
  if (safety.class === "mixed-safe-sensitive") {
    return {
      status: "rejected",
      reason:
        "query mixes safe and sensitive fragments; please rephrase the query to remove the sensitive part",
      safetyClass: "mixed-safe-sensitive",
    };
  }
  // Note: we intentionally do NOT route prompt-injection /
  // unsafe-preference / raw-dump / self-conflict / vague-junk
  // inputs to a hard reject here. The recall path is read-only
  // and read-only queries are less dangerous than remember
  // inputs (no persistence, no per-record mutation). A vague
  // query simply yields no matches. The downstream lexical
  // ranker handles them naturally.

  // -- 2. Read safe stored summaries ----------------------------------
  const summaries = listActiveMemorySummaries(storage, {
    limit: storageLimit,
  });
  logger.debug(
    `recall: read ${summaries.length} safe summary(ies) from storage`,
  );
  // Parallel lookup of stored `relationship` blocks
  // (Phase C). The controller uses this only for the
  // internal `internalAmbiguity` field on the answered
  // outcome. The public recall pipeline does not surface
  // these blocks in any other way. The two read functions
  // share the same `state = 'active'` filter and the same
  // ascending `id` order, so the controller can project
  // the block onto a candidate by id lookup below.
  const relationshipBlocks = listActiveMemoryRelationshipBlocks(storage, {
    limit: storageLimit,
  });
  const blockById = new Map<number, (typeof relationshipBlocks)[number]["block"]>();
  for (const r of relationshipBlocks) {
    blockById.set(r.id, r.block);
  }

  // -- Phase 3A trace: active memory read --------------------------------
  emitActiveMemoryReadStage(options.trace, {
    readCount: summaries.length,
    storageLimit,
  });

  // -- 3. Lexical ranking ---------------------------------------------
  const ranked: LexicalScoredCandidate[] = rankLexical(
    query,
    summaries.map((s: SafeMemorySummary) => ({
      id: s.id,
      text: s.memoryContent,
      tags: s.tags,
    })),
    { threshold, topK },
  );
  // Build the id->summary map once; used by both the trace
  // event and the topSummaries construction below.
  const summaryById = new Map<number, SafeMemorySummary>(
    summaries.map((s) => [s.id, s]),
  );
  // -- Phase 3A trace: lexical ranking ---------------------------------
  emitLexicalRankingStage(options.trace, {
    query,
    threshold,
    topK,
    ranked,
    summariesById: summaryById,
  });
  if (ranked.length === 0) {
    logger.debug("recall: no relevant memory; skipping provider call");
    return { status: "no_memory" };
  }
  // Re-attach summaries in the order returned by the ranker.
  // ranked already carries the score-sorted order. We use a
  // single O(N) map lookup so this stays linear even if the
  // store grows. The relationship block (Phase C) is
  // attached as an optional extension; rows without a stored
  // block carry `relationship: undefined` and the detector
  // treats them as having no stored data (it can still fire
  // on the lexical / asymmetric-negation path).
  const topSummaries: SafeMemorySummaryWithRelationship[] = [];
  for (const r of ranked) {
    const s = summaryById.get(r.id);
    if (!s) continue;
    const block = blockById.get(r.id);
    topSummaries.push(
      block === undefined
        ? { ...s }
        : { ...s, relationship: { ...block } },
    );
  }
  // Sanity: every ranked id must have a summary. If this ever
  // fails, it means a memory was deleted between the read and
  // the rank — fall back to no_memory rather than fabricating.
  if (topSummaries.length !== ranked.length) {
    logger.debug(
      "recall: ranked-id missing summary (storage raced); returning no_memory",
    );
    return { status: "no_memory" };
  }

  // -- Phase 3A trace: selected candidates -----------------------------
  emitSelectedCandidatesStage(options.trace, topSummaries);

  // -- 4. Provider synthesis -----------------------------------------
  const adapterOptions: RecallSynthesisOptions = {};
  if (options.providerFetchImpl !== undefined) {
    adapterOptions.fetchImpl = options.providerFetchImpl;
  }
  if (options.providerPrimaryApiKey !== undefined) {
    adapterOptions.primaryApiKey = options.providerPrimaryApiKey;
  }
  if (options.providerFallbackApiKey !== undefined) {
    adapterOptions.fallbackApiKey = options.providerFallbackApiKey;
  }
  if (options.providerPrimaryBaseUrl !== undefined) {
    adapterOptions.primaryBaseUrl = options.providerPrimaryBaseUrl;
  }
  if (options.providerFallbackBaseUrl !== undefined) {
    adapterOptions.fallbackBaseUrl = options.providerFallbackBaseUrl;
  }
  if (options.providerPrimaryModel !== undefined) {
    adapterOptions.primaryModel = options.providerPrimaryModel;
  }
  if (options.providerFallbackModel !== undefined) {
    adapterOptions.fallbackModel = options.providerFallbackModel;
  }

  const memories: RecallMemoryInput[] = topSummaries.map((s) => ({
    id: s.id,
    memoryContent: s.memoryContent,
    kind: s.kind,
    tags: s.tags,
  }));
  const result: RecallSynthesisResult = await synthesizeRecallWithFallback(
    query,
    memories,
    adapterOptions,
  );

  if (!result.ok) {
    logger.debug(`recall: provider error: ${result.kind} (${result.message})`);
    return {
      status: "provider_error",
      reason: result.message,
    };
  }

  // -- 5. Validate the synthesized answer ----------------------------
  const validation = validateAnswer(result.answer);
  if (!validation.ok) {
    if (validation.reason === "refusal_detected") {
      // Refusal-with-thin-match reroute. The synthesis LLM
      // declined to answer (the `isProviderRefusal` detector
      // fired). If the lexical ranker found at least one
      // stored summary above the relevance threshold, the
      // "no relevant memory was found" framing of `no_memory`
      // would be misleading — the ranker DID find something,
      // it just could not be synthesized into a confident
      // answer. In that case we surface a `weak_match`
      // outcome that exposes the top-3 curator-voice
      // summaries and a coverage block, so the caller can
      // inspect the closest stored memories instead of
      // receiving a blank "No relevant memory found.".
      //
      // If the ranker found nothing (`topSummaries.length
      // === 0`), the refusal is genuinely "no memory to
      // reason about" and we fall back to `no_memory` as
      // before. Note: in practice `topSummaries` is
      // guaranteed non-empty here — the controller
      // short-circuits with `no_memory` at the rank-empty
      // branch above — but the explicit check keeps the
      // reroute site defensive and matches the brief's
      // trigger condition (`topSummaries.length > 0`).
      if (topSummaries.length > 0) {
        logger.debug(
          "recall: provider answer was a refusal; rerouting to weak_match",
        );
        return {
          status: "weak_match",
          summaries: topSummaries.slice(0, 3).map((s) => s.memoryContent),
          coverage: {
            topScore: ranked[0]!.score,
            supportingCount: ranked.length,
          },
        };
      }
      logger.debug(
        "recall: provider answer was a refusal; rerouting to no_memory",
      );
      return { status: "no_memory" };
    }
    logger.debug(
      `recall: provider answer rejected: ${validation.reason}`,
    );
    return {
      status: "provider_error",
      reason: validation.reason,
    };
  }
  // Use the redacted form so any secret-shaped fragment the
  // model produced (defense in depth) is removed before we hand
  // the text to the tool layer. The validator above already
  // rejected answers that are entirely or mostly redacted.
  const safeAnswer = validation.answer;

  // -- 6. Internal ambiguity detection (Phase C) ----------------------
  // Conservative offline detector. The signal is attached
  // to the answered outcome as an internal-only field.
  // `src/tools/recall.ts` is responsible for dropping it
  // from the public projection. The public message / answer
  // remain byte-equal to pre-Phase-C for every status
  // branch. The detector does NOT short-circuit the
  // pipeline; the provider call above always runs.
  const internalAmbiguity: AmbiguitySignal = detectAmbiguity({
    topCandidates: topSummaries,
    answer: safeAnswer,
  });

  // -- 7. Internal resolved-history detection (Phase H + J) -----------
  // Conservative offline detector that recognises an
  // **explicit** resolution marker pair (`previous` /
  // `old` / `superseded` / `no longer` on the previous
  // side; `current` / `replaced` on the current side),
  // with a referential-pointer variant (Pattern B in spec
  // §5.2). The detector is structurally parallel to the
  // Phase C ambiguity detector above but uses a different
  // rule set; the two signals live on separate internal
  // fields on the answered outcome (see the
  // `internalResolvedHistory` field on `RecallOutcome`).
  // The detector does NOT short-circuit the pipeline.
  // `src/tools/recall.ts` is responsible for the public
  // projection: it composes `formatAmbiguityNote` and
  // `formatResolvedHistoryNote` into a single prefix on
  // the public `message` (the ambiguity note takes
  // precedence per spec §4.2 / §4.3: a stored conflict
  // pointer wins, and the detector stays silent on
  // resolved-history when the Phase D detector would
  // fire). When both notes are empty, the public
  // `message` is byte-equal to the synthesized answer
  // (pre-Phase-J invariant, preserved for the no-detector
  // case).
  const internalResolvedHistory: ResolvedHistorySignal = detectResolvedHistory({
    topCandidates: topSummaries,
    answer: safeAnswer,
  });

  return {
    status: "answered",
    answer: safeAnswer,
    sourceIds: topSummaries.map((s) => s.id),
    internalAmbiguity,
    internalResolvedHistory,
  };
}

